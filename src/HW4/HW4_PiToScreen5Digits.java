package HW4;

public class HW4_PiToScreen5Digits {

	public static void main(String[] args) {
		
		
		System.out.println("Full Pi looks like " + Math.PI);
		
		System.out.println(" Showing PI shorter");
		System.out.println(String.format("%.1g %n", Math.PI));
		System.out.println(String.format("%.2g %n", Math.PI));
		System.out.println(String.format("%.3g %n", Math.PI));
		System.out.println(String.format("%.4g %n", Math.PI));
		System.out.println(String.format("%.5g %n", Math.PI));
		
		

		
	}

}
